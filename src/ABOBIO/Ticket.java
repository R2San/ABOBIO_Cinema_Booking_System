    private void txQuantityStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_txQuantityStateChanged
        // TODO add your handling code here:
        try{
            String type = (String)txType.getSelectedItem();
            
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            
            String sql = "SELECT type,price FROM movietype WHERE type ='" + type + "'";
            ResultSet m = s.executeQuery(sql);
            
            
            if(m.next()){
                
                int price = m.getInt("price");
                if((int)txQuantity.getValue() < 1){
                    txQuantity.setValue(1);
                    txQuantity.requestFocus();
                }
                int total = price * (int)txQuantity.getValue();
                txPrice.setText(Integer.toString(total));
            }

        }catch(SQLException e){
            System.out.println("Error");
        }
        
    }//GEN-LAST:event_txQuantityStateChanged

