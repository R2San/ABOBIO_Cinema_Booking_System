        // TODO add your handling code here:
        String trId = Payment.txTrId.getText().trim();
        String username = Dashboard.txUsername1.getText().trim();
        String movieid = ListMovie.txMovieId.getText().trim();
        String type = txType.getSelectedItem().toString().trim();
        String quantity1 = txQuantity.getValue().toString().trim();
        String seat = Payment.txSeat.getText().trim();
        String ticketid = txTicket.getText().trim();
        String price = txPrice.getText().trim();
        
        int quantity = Integer.parseInt(quantity1);
        
        if(quantity < 1){
            JOptionPane.showMessageDialog(null, "Quantity cannot be under 1");
        }
        else{

            try{
                Connection c = Koneksi.getKoneksi();
                String sql = "INSERT INTO transactiondetails VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
                PreparedStatement p = c.prepareStatement(sql);
                
                p.setString(1,trId);
                p.setString(2,movieid);
                p.setString(3,type);
                p.setInt(4,quantity);
                p.setString(5,ticketid);
                p.setString(6,seat);
                p.setString(7,price);
                p.setString(8,username);

                p.executeUpdate();
                p.close();
                JOptionPane.showMessageDialog(null,"Payment Success");
                
            }catch(SQLException e){
                System.out.println("Errora");
                
            }

            finally{
                this.dispose();
                Payment a = new Payment();
                a.setVisible(true);
            
            
            
           
        }

    }//GEN-LAST:event_jButton2ActionPerformed
