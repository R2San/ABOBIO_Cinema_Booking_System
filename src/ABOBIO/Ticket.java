    public void autoNumber(){
        try{
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT transactionid FROM transactiondetails ORDER BY transactionid DESC";
            ResultSet r = s.executeQuery(sql);
            
            if(r.next()){
                String NoID = r.getString("transactionid").substring(2);
                String ID = "" + (Integer.parseInt(NoID)+1);
                String Zero = "";
                
                if(ID.length()==1){
                    Zero="00";
                }
                else if(ID.length()==2){
                    Zero="0";
                }
                else if(ID.length()==3){
                    Zero="";
                }
                
                Payment.txTrId.setText("TR" + Zero + ID);
            }
            else{
                Payment.txTrId.setText("TR001");
            }
            r.close();
            s.close();
            
        }catch(Exception e){
            System.out.println("autonumber error");
        }
    }
    
